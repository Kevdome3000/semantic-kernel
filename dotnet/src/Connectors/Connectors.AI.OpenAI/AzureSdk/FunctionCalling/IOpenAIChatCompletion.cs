namespace Microsoft.SemanticKernel.Connectors.AI.OpenAI.AzureSdk.FunctionCalling;

using System.Collections.Generic;
using System.Threading;
using System.Threading.Tasks;
using Azure.AI.OpenAI;
using SemanticKernel.AI.ChatCompletion;
using SemanticKernel.AI.TextCompletion;


public interface IOpenAIChatCompletion : IChatCompletion, ITextCompletion
{
    /// <summary>
    /// Get chat completion results for the prompt and settings.
    /// </summary>
    /// <param name="chat">The chat history context.</param>
    /// <param name="requestSettings">Request settings for the completion API</param>
    /// <param name="functionCall"> The function call to be made.</param>
    /// <param name="functionCalls"> The available function calls.</param>
    /// <param name="cancellationToken">The <see cref="CancellationToken"/> to monitor for cancellation requests. The default is <see cref="CancellationToken.None"/>.</param>
    /// <returns>List of different chat results generated by the remote model</returns>
    Task<IReadOnlyList<IChatResult>> GetChatCompletionsAsync(
        ChatHistory chat,
        ChatRequestSettings? requestSettings = null,
        FunctionDefinition? functionCall = null,
        FunctionDefinition[]? functionCalls = null,
        CancellationToken cancellationToken = default);
}
